USE [GMIDATA]
GO

SET NOCOUNT ON

SELECT
COLUMN_NAME +
	CASE DATA_TYPE
		WHEN 'bigint' THEN ' ' + UPPER(DATA_TYPE) + ','
		WHEN 'char' THEN ' ' + UPPER(DATA_TYPE) + '(' + CONVERT(VARCHAR,CHARACTER_MAXIMUM_LENGTH) + ')' + ','
		WHEN 'date' THEN ' ' + UPPER(DATA_TYPE) + ','
		WHEN 'datetime' THEN ' ' + UPPER(DATA_TYPE) + ','
		WHEN 'datetime2' THEN ' ' + UPPER(DATA_TYPE) + '(7)' + ','
		WHEN 'decimal' THEN ' ' + UPPER(DATA_TYPE) + '(' + CONVERT(VARCHAR,NUMERIC_PRECISION) + ',' + CONVERT(VARCHAR,NUMERIC_SCALE) + ')' + ','
		WHEN 'int' THEN ' ' + UPPER(DATA_TYPE) + ','
		WHEN 'numeric' THEN ' ' + UPPER(DATA_TYPE) + '(' + CONVERT(VARCHAR,NUMERIC_PRECISION) + ',' + CONVERT(VARCHAR,NUMERIC_SCALE) + ')' + ','
		WHEN 'money' THEN ' ' + UPPER(DATA_TYPE) + ','
		WHEN 'smallint' THEN ' ' + UPPER(DATA_TYPE) + ','
		WHEN 'tinyint' THEN ' ' + UPPER(DATA_TYPE) + ','
		WHEN 'varchar' THEN ' ' + UPPER(DATA_TYPE) + '(' + CONVERT(VARCHAR,CHARACTER_MAXIMUM_LENGTH) + ')' + ','
		ELSE ' [UNHANDLED DATA_TYPE]'
	END
FROM INFORMATION_SCHEMA.COLUMNS
WHERE (TABLE_NAME = 'GMI_SOD_Positions_Detailed')
ORDER BY ORDINAL_POSITION

SET NOCOUNT OFF
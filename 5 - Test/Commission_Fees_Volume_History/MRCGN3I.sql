USE [GMIDATA]
GO

SET NOCOUNT ON

DECLARE
@PACCT VARCHAR(5)

SET @PACCT='97200'

SELECT
PACCT,
PEXCH,
PFC,
PSTRIK,
PSUBTY,
Expiry,
PSDSC1,
PCURAT,
SUM(Quantity) "Total Quantity",
SUM(PCOMM) "Comm Amt",
SUM(PFEE1 + PFEE2 + PFEE3 + PFEE4 + PFEE5 + PFEE6 + PFEE7 + PFEE8 + PFEE9 + PGICHG + PBKCHG + POTHER + PMBKOF + PMFLOR + PMORDR + PMWIRE + PMOTHR) "Total Fees"
FROM [dbo].[Commission_Fees_Volume_History]
WHERE (PACCT=@PACCT)
AND (PRECID NOT IN('P','R'))
GROUP BY
PACCT,
PEXCH,
PFC,
PSTRIK,
PSUBTY,
Expiry,
PSDSC1,
PCURAT
ORDER BY
PACCT,
PEXCH,
PFC,
PSTRIK,
PSUBTY,
Expiry,
PSDSC1,
PCURAT

SET NOCOUNT OFF